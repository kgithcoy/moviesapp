package android.moviesapp.domain;

public class Person {
    private int id;
    private String name;
    private String profilePath;
    private String character;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getProfilePath() {
        return profilePath;
    }

    public String getCharacter() {
        return character;
    }
}
